// A CONCRETE TYPE specifies:
// - not only what its values can DO: what methods you can call on them,
// - but also what they ARE: the underlying type that holds the value's data.

// INTERFACE type
// - ONLY describe what a value can DO: what methods it has.
// - DON'T describe what a value IS: they don't say what its underlying type is, or how its data is stored.

