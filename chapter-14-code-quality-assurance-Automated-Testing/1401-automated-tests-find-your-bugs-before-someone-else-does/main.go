package main

import "fmt"

func main() {
	fmt.Println("What is an automated test?")
	// An AUTOMATED TEST is a separate program that
	// executes parts of your main program, and
	// verifies they behave as EXPECTED.

	fmt.Println("You run your programs every time I add a new feature, to test it out. Isn't that enough?")
	// Not unless you're going to test all the OLD features as well,
	// to make sure your changes haven't broken anything.

	fmt.Println("Automated test and manual testing?")
	// Automated test SAVE TIME over manual testing,
	// and they're usually more THOROUGH, too.
}
